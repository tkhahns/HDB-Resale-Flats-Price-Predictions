def generate_synthetic_macro(output_path: str = _MACRO_CSV) -> pd.DataFrame:
    """Write the built-in synthetic macro series to disk and return it."""
    import io

    df = pd.read_csv(io.StringIO(_SYNTHETIC_ROWS))
    df["month"] = pd.to_datetime(df["month"], format="%Y-%m")
    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(output_path, index=False)
    logger.info("Wrote synthetic macro data (%d months) to %s", len(df), output_path)
    return df


def load_macro_from_csv(path: str = _MACRO_CSV) -> pd.DataFrame:
    df = pd.read_csv(path)
    df["month"] = pd.to_datetime(df["month"])
    return df


def apply_lag(macro_df: pd.DataFrame, lag_months: int = 1) -> pd.DataFrame:
    """Shift macro series forward by `lag_months` so transactions at time T
    only see macro values known as of T-lag (leakage guard)."""
    out = macro_df.copy()
    out["month"] = out["month"] + pd.DateOffset(months=lag_months)
    return out
